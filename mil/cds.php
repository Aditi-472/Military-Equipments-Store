            Every year UPSC conducts CDS Exam twice a year for the candidates who wish to begin their career in the Indian Air Force, Indian Military Academy, Indian Naval Academy and Officer’s Training Academy. The candidates qualifying for the written test will be called for an interview round and medical examination for final selection.</p>
            <h2 class="text-warning fs-1">CDS EXAM APPLICATION FEE</h2>  
            <p class="text-align:justify;">The examination fee for CDS Exam is Rs. 200/- for all categories except SC/ST/Females (exempted from fee payment). The payment can be done through online/offline mode (Net Banking, Mastercard/Debit Card or cash deposit in any branch of SBI Bank.)</p>
