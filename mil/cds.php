            <p class="text-align:justify;">The recruitment of AFA, IMA, INA & Officers Training Academy posts will be conducted on the basis of: 1. Written Exam 2. SSB Interview/Personality Test 3. Document Verification 4. Medical Examination</p>
            <h2 class="text-warning fs-1">CDS EXAM PATTERN</h2>
            <p class="text-align:justify;">The candidates have to clear CDS Written Examination to get selected for the interview round. Check the exam pattern for each position below.
