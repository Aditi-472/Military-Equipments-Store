            <br>Step 4: Read all the instructions carefully and proceed by clicking on "Yes"
            <br>Step 5: You will be redirected to a new page, start filling in your details correctly.
            <br>Step 6: Select your preferences carefully and continue to Part-II.
            <br>Step 7: Pay the required application fee through online/offline mode as discussed above.
            <br>Step 8: Select the desired examination center from the list.
            <br>Step 9: Upload scanned documents as required in the application form.
            <br>Step 10: Check all the details and click on "Submit". Download a copy of the application form for future reference.</p>
            <h2 class="text-warning fs-1 ">CDS EXAM ELIGIBILITY CRITERIA</h2>
            <p style="text-align:justify;">The candidates must meet the eligibility standards as set by the Union Public Service Commission for Defence recruitment. Check detailed eligibility (nationality, education, age, marital status, sex) from below.
            <br>Nationality
            <br>A candidate should hold The Citizenship of India, subject of Nepal/Bhutan, or Tibetan refugee who had been migrated to India before January 01, 1962, with the intention of Permanent Settle, or An individual of Indian Origin migrated from Pakistan, Sri Lanka, Burma, Uganda, Kenya, Tanzania, Ethiopia, Malawi, Zambia, Zaire & Vietnam for permanent settle purpose in India. .
Education Qualification
            <br>For I.M.A. and Officers’ Training Academy - The candidate should have a graduation degree (any subject) from a recognized University or equivalent.
            <br>For Indian Naval Academy- The candidate should have completed their Graduation in Engineering from a recognized University/Institution.
            <br>For Air Force Academy- The candidate should hold a bachelor's degree from a recognized University (with Physics and Mathematics at 10+2 level) or Bachelor of Engineering.
            <br>Age Limit The candidate should fall in the age group of 20 to 24 years. The detailed age limit details are mentioned on the official notification pdf.
            <br>Marital Status & Sex • For Indian Military Academy, Indian Naval Academy, only Unmarried males are eligible to apply for CDS Exam • For Indian Air Force Academy, both married and unmarried males can apply, however marriage during the training is not permitted • For Officer Training Academy, unmarried males & females can apply for CDS Exam. .            
