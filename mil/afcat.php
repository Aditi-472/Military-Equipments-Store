        <strong class="fs-5">Selection Process:-</strong>
        <p style="text-align:justify;">Indian Air Force sends call letters to all the candidates who clear the AFCAT/EKT to appear for the second stage of selection, i.e. AFSB Testing.</p>    
        <strong class="fs-5">AFSB Testing Stage 1:-</strong>    
        <p style="text-align:justify;">This includes the following two tests:-
        1. Officer Intelligence Rating Test
        2. Picture Perception and Discussion Test (PP & DT) <br>Stage 1 is called the screening test and candidates who do not clear it will be rejected and will not be called for AFSB Testing Stage 2.</p>
        <strong class="fs-5">AFSB Testing Stage 2:-</strong>    
        <p style="text-align:justify;">All Stage 1 qualified candidates will have to appear for AFSB Stage 2 and undergo the following tests:-
        <br>• Psychological Tests: - These are written psychological tests and conducted by professional psychologists.
        <br>• Group Tests: - These tests involve mental and physical activities to examine the candidates on various mental and physical parameters.
        <br>• AFSB Interview: - In this round, the interviewing officer takes a personal round of interviews for the candidates.
        <br>• Computerised Pilot Selection System (CPSS) Test:- This test is mandatory for only those candidates who have applied for the Flying Branch.</p>
        <strong class="fs-5">Medical Examination:-</strong>    
        <p style="text-align:justify;">Recommended candidates will be called for medical examination. After the medical examination, a final merit list will be prepared on the basis of the marks scored by the candidates in written and AFSB interviews. The shortlisted candidates would be given joining letters of the respective training academies they have applied to.</p>
        <strong class="fs-5">AFCAT EXAM:-</strong>    
        <p style="text-align:justify;">Type of Questions: - Multiple Choice Questions
        <br>Time Duration: - 2 Hrs.
        <br>Number of Questions: - 100
        <br>Max. Marks: - 300
        <br>Subjects: - Verbal Ability, Numerical Ability, Reasoning, General Awareness and Military Aptitude.
        <br>There is a negative marking of 1/3rd mark in all students.
        <br>Engineering Knowledge Test (EKT) would be for 45 minutes and conducted immediately after AFCAT.
        <br>No. of Questions: - 50
        <br>Max. Marks: - 150
        <br>Note: - Candidates who are opting for Ground Duty (Technical) branch have to pass both AFCAT and EKT.
        </p>
