        <p style="text-align:justify;">Air Force Common Admission Test (AFCAT) is an entrance examination conducted by Indian Air Force to select eligible and desirable candidates for multiple posts in Flying and Ground Duty (Technical and Non – Technical). Serving in such a commendable and respected field is an honour in itself. AFCAT exam is conducted twice every year by Indian Air Force (IAF) and millions of students appear to ace this exam and join reputable Air Force Academy and get a commission as an Air Force Officer. This adventurous field offers possibility to both male and female candidates to serve. To crack the exam with good marks, aspirants must be well – versed with the AFCAT Syllabus, Exam Pattern, Eligibility Criteria and Selection Process.</p>
        <strong class="fs-5">AFCAT Age Limit:-</strong>
        <p style="text-align:justify;">(1) Flying Branch: - Applicant’s age should be at least 20 years and the upper age is capped at 24 years for flying branch. Candidates below 25 years of age must be unmarried at the time of commencement of the course. Upper age limit for candidates holding valid and current Commercial Pilot License issued by DGCA (India) is relaxable upto 26 years
        (2) Ground Duty (Technical/Non-Technical) Branches: For ground duty, the minimum age is 20 years and the upper age limit is capped at 26 years.
        Note: Candidates below 25 years of age must be unmarried at the time of commencement of the course. Widows/Widowers and divorcees (with or without encumbrances) below 25 years of age are also not eligible.</p>    
        <strong class="fs-5">AFCAT Educational Qualification:-</strong>
        <p style="text-align:justify;">Flying Branch:- Minimum 60% marks each in physics and mathematics at 10+2 level and a three – year degree in graduation or a BE/B Tech degree with a minimum of 60% marks, from a recognised university.</p>    
